import java.util.*;
import java.util.HashMap;

public class UsingHashMap {
    public static void main(String[] args) {
        
        HashMap hm = new HashMap<>();
        hm.put(45, "Hello");
        hm.put(96, "How");
        hm.put(23, "are");
        hm.put(93, "you");
        hm.put(12, "doing?");

        System.out.println(hm);

    }
    
}
